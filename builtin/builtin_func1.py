#all(iterable),return True is all member of iterable is true
list1 = [1,2,3,None]
list2 = [1,2,3,4]
print(all(list1))
print(all(list2))

#any(iterable),return True if any element of the iterable is true. 
list1 = [1,2,3,None]
list2 = [1,2,3,4]
print(any(list1))
print(any(list2))